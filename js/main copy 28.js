/*
    Logical Operators
    -- ! Not
    -- && And
    -- || Or
*/

console.log(true);
console.log(!true);
console.log(false);
console.log(!false);

console.log(!(10 == "10"));

console.log(10 == "10" && 10 > 8 && 10 > 50); // && > And  
console.log(10 == "10" || 10 > 8 || 10 > 50); // || > Or  